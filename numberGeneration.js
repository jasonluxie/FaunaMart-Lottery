function generateUniqueDigits(start, end) {
  const numbers = [];
  for (let i = start; i <= end; i++) {
    const digits = i.toString().split('');
    if (new Set(digits).size === digits.length) {
      numbers.push(i);
    }
  }
  return numbers;
}

